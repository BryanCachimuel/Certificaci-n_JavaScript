/* Operadores Lógicos */
let a = true;
let b = false;

// Operador && (and o Y)
// Regresa verdadero si ambos operandos son verdaderos
console.log(`${a} && ${b} -> ${a && b}`);

// Operador Lógico || (or o O)
//  Regresa verdadero si cualquiera de los valores es verdadero
console.log(`${a} || ${b} -> ${a || b}`);

// Operador ! (not o No)
// Invierte el valor original. true -> false y false -> true
console.log(`${a} -> ${!a}`);
console.log(`${b} -> ${!b}`);